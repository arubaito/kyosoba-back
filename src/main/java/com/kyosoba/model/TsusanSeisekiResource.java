package com.kyosoba.model;

/**
 * 競走馬の通算成績を保持するリソース
 */
public class TsusanSeisekiResource {
	private int syobusu;
	private int syorisu;

	public int getSyobusu() {
		return syobusu;
	}
	public void setSyobusu(int syobusu) {
		this.syobusu = syobusu;
	}
	public int getSyorisu() {
		return syorisu;
	}
	public void setSyorisu(int syorisu) {
		this.syorisu = syorisu;
	}
	

}
